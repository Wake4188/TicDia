// Get a deterministic subset of words for a specific date
// This ensures variety day-to-day while keeping consistency within the same day
export function getDailyWordPool(date: Date, poolSize: number = 50): string[] {
  const dateString = date.toISOString().split('T')[0];
  const seed = parseInt(dateString.replace(/-/g, ''), 10);
  
  // Seeded random number generator
  let currentSeed = seed;
  const seededRandom = () => {
    currentSeed = (currentSeed * 1103515245 + 12345) & 0x7fffffff;
    return currentSeed / 0x7fffffff;
  };
  
  // Create a shuffled copy using the seed
  const shuffled = [...INTERESTING_WORDS];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(seededRandom() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  
  return shuffled.slice(0, poolSize);
}

// Get random words from a pool, excluding already seen words
export function getRandomWordsFromPool(
  pool: string[],
  count: number,
  exclude: Set<string> = new Set()
): string[] {
  const available = pool.filter(w => !exclude.has(w));
  const shuffled = [...available].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
